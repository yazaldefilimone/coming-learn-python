# os tipos boolean sao valores que assumem veradeiro ou falso.

print(True);
print(False);


# retorna  True(verdadeiro) se todos os valores forem veradeiros-(True)
print(True and True)
print(True and False)
print(False and False)

# retorna  True(verdadeiro) se um dos valores for veradeiro(True)
print(True or True)
print(True or False)
print(False or False)

# podemos negar uma valor boolean ele se torna o valor oposto.

print(not True) #False.
print(not False) #True
# pode percever que os primiros caracteres sao e masculino.

